    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser jfc = new JFileChooser();
        int returnValue = jfc.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = jfc.getSelectedFile();
            fieldArquivo.setText(selectedFile.getName());

            try (BufferedReader br = new BufferedReader(new FileReader(selectedFile))) {
                String line = br.readLine();

                while (line != null) {
                    if (!line.isEmpty()) {
                        String[] values = line.split(";");

                        Banho b = new Banho();
                        b.setNome(values[0]);
                        b.setSigla(values[1]);
                        b.setValorGrama(new BigDecimal(values[2]));

                        banhos.add(b);

                    }
                    line = br.readLine();
                }

            } catch (IOException ex) {
                Logger.getLogger(FrmImportBanho.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (!banhos.isEmpty()) {
                fieldTabela.setModel(new TableBanho((List<Banho>) banhos, null));
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

