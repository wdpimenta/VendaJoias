    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {

            long id = 0L;
            try {
                id = Long.parseLong(jClienteCodigo.getText());
            } catch (NumberFormatException e) {

            }
            Date dtIni = null;
            try {
                dtIni = Util.sdf.parse(jTdfDataIni.getText());
            } catch (ParseException e) {

            }
            Date dtFim = null;
            try {
                dtFim = Util.sdf.parse(jTfdDataFim.getText());
            } catch (ParseException e) {

            }

            EStatus status = jRdAberto.isSelected() ? EStatus.ABERTO : (jRdFechado.isSelected() ? EStatus.FECHADO : null);

            if (status != null) {
                if (dtFim != null && dtIni != null) {
                    findAllByCliente = pedidoDao.findAllByClienteAndDtIniAndDtFimAndStatus(id, dtIni, dtFim, status);
                } else if (dtFim == null && dtIni != null) {
                    findAllByCliente = pedidoDao.findAllByClienteAndDtIniAndStatus(id, dtIni, status);
                } else if (dtFim != null && dtIni == null) {
                    findAllByCliente = pedidoDao.findAllByClienteAndDtFimAndStatus(id, dtFim, status);
                } else {
                    findAllByCliente = pedidoDao.findAllByClienteAndStatus(id, status);
                }
            } else if (dtFim != null && dtIni != null) {
                findAllByCliente = pedidoDao.findAllByClienteAndDtIniAndDtFim(id, dtIni, dtFim);
            } else if (dtFim == null && dtIni != null) {
                findAllByCliente = pedidoDao.findAllByClienteAndDtIni(id, dtIni);
            } else if (dtFim != null && dtIni == null) {
                findAllByCliente = pedidoDao.findAllByClienteAndDtFim(id, dtFim);
            } else if (id > 0) {
                findAllByCliente = pedidoDao.findAllByCliente(id);
            } else {
                findAllByCliente = pedidoDao.findAllByCliente(0);
            }

            if (!findAllByCliente.isEmpty()) {
                doClose(RET_OK);

            } else {
                JOptionPane.showMessageDialog(this, "Sem registro para gerar relatório");

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, String.format("Revise os dados e solicite a consulta novamente [%s]", ex.getMessage()));
        }

    }//GEN-LAST:event_okButtonActionPerformed

