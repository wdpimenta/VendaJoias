    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FrmConsultaCliente frmConsultaCliente = new FrmConsultaCliente(this, true);
        frmConsultaCliente.setVisible(true);

        if (frmConsultaCliente.getReturnStatus() == FrmConsultaCliente.RET_OK) {
            jClienteCodigo.setText(frmConsultaCliente.getClienteSelecionado().getId().toString());
            jClienteNome.setText(frmConsultaCliente.getClienteSelecionado().getNome());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jClienteCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jClienteCodigoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            long aLong = Util.getLong(jClienteCodigo);
            if (aLong > 0) {
                jClienteNome.setText("");
                Cliente c = clienteDao.find(aLong);

                if (c != null) {
                    jClienteCodigo.setText(c.getId().toString());
                    jClienteNome.setText(c.getNome());
                } else {
                    JOptionPane.showMessageDialog(this, String.format("Cliente com o código [%s] infromado não encotrando", aLong));
                }

            }
        }
    }//GEN-LAST:event_jClienteCodigoKeyPressed

