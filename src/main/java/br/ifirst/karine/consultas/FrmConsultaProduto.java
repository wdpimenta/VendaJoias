    private void consultarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarButtonActionPerformed
        String text = fieldValorConsulta.getText().trim();
        if (!text.isEmpty()) {
            
            List<Produto> produtos = null;
            
            int selectedIndex = fieldTipoConsulta.getSelectedIndex();
            if(selectedIndex == 0){
                produtos = produtoDao.findAllByNome(text);
            }else{
                produtos = produtoDao.findAllByReferencia(text);
            }

            if (produtos != null) {
                fieldProdutos.setModel(new TableProduto(produtos, null));
            }
        }
    }//GEN-LAST:event_consultarButtonActionPerformed

    private void fieldProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fieldProdutosMouseClicked
        if (evt.getClickCount() == 2) {
            int selectedRow = fieldProdutos.getSelectedRow();
            if (selectedRow > -1) {
                TableProduto model = (TableProduto) fieldProdutos.getModel();
                produtoSelecionado = model.get(selectedRow);
                doClose(RET_OK);
            }
        }
    }//GEN-LAST:event_fieldProdutosMouseClicked

    private void fieldValorConsultaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldValorConsultaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            consultarButton.doClick();
        }
    }//GEN-LAST:event_fieldValorConsultaKeyPressed

    private void doClose(int retStatus) {
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }


